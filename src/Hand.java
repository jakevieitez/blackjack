
public class Hand {

	private Card firstCard;
	private Card secondCard;
	
	
	public Hand (Card card1, Card card2) {
		
		firstCard = card1;
		secondCard = card2;
		
	}


	public Card getFirstCard() {
		
		return firstCard;
		
	}
	
	public Card getSecondCard() {
		
		return secondCard;
		
	}
	


}
